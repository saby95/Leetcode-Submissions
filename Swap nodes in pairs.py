# class ListNode(object):
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution(object):
    def swapPairs(self, head):
        """
        :type head: ListNode
        :rtype: ListNode
        """
        if head==None:
            return None
        if head.next==None:
            return head
        else:
            tmp = head.next
            tmp1 = tmp.next
            tmp.next=head
            head.next = self.swapPairs(tmp1)
        return tmp

